 let str="Rohit"
 str[3]='b'    //string are immutable in JavaScript means we can't change the character of string at any index
 console.log(str)    //output: Rohit

 let fruits = ["apple", "banana", "cherry"];
 fruits[3]='Rohit'    //Array are mutable in JavaScript means we can change the  element of array at any index
 console.log(fruits) //output : [ 'apple', 'banana', 'cherry', 'Rohit' ]
